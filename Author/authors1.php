<h3>About</h3>
<p> Cumhuriyet döneminin önemli romancılarından olan Orhan Kemal, 15 Eylül 1914 tarihinde Adana’da dünyaya geldi. 
    Adana’nın Fransızlar tarafından işgal edilmesi üzerine ailesiyle birlikte bir süre Niğde ve Konya’da kaldı.
    1931 yılında öğrenimini yarıda bırakarak ailesiyle birlikte bu kez Beyrut’a gitti.
    Bir yıl burada kaldıktan sonra Adana’ya dönerek babaannesinin yanına yerleşti. 
    Bir süre çeşitli işlerde çalıştıktan sonra askere gitti.
    Askerlik görevi sırasında “Nazım Hikmet ve Maksim Gorki’nin kitaplarını okumak” ve “Yabancı rejimler lehine propaganda” suçlaması ile karşı karşıya kaldı.
    Yargılama sonucunda 5 yıl hapis cezasına çarptırıldı.
    “Duvarlar” başlıklı ilk şiiri, 1939 yılında Yedigün adlı dergide yayımlandı.
    Cezaevinde Nazım Hikmet ile tanışan Orhan Kemal, onun teşvikleri sonucunda roman ve öykü yazmaya yöneldi.
    Cezaevinden çıktıktan sonra bir süre hamal olarak çalıştı.
    1948 yılında ilk öykü kitabı “Duygu“, ertesi yıl da ilk romanı “Baba Evi” yayımlandı.
    1958 yılında “Kardeş Payı” öyküsü ile Sait Faik Hikaye Armağanı‘nı kazandı. 1969 yılında “Önce Ekmek” adlı eseri ile tekrardan Sait Faik Hikaye Armağanı‘nı ve Türk Dil Kurumu Ödülü‘nü kazandı.
    Orhan Kemal, 2 Haziran 1970 tarihinde Sofya’da hayatını kaybetti.
</p>


